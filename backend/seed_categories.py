for cat_name, skills in data.items():
    # Make a clean slug
    slug = cat_name.lower().replace(" ", "-").replace("&", "and").replace(",", "").replace("/", "-")
    cat, created = Category.objects.get_or_create(name=cat_name, slug=slug)
    for skill_name in skills:
        skill_slug = skill_name.lower().replace(" ", "-").replace("&", "and").replace(",", "").replace("/", "-").replace("(", "").replace(")", "")
        # Ensure unique skill slug by prefixing category slug
        unique_skill_slug = f"{cat.slug}-{skill_slug}"
        Skill.objects.get_or_create(category=cat, name=skill_name, slug=unique_skill_slug)

